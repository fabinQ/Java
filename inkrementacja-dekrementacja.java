class Scratch {
    public static void main(String[] args) {
        int number_1 = 2;
        int number_2 = 5;
        System.out.println(number_1++ + number_2++);
        System.out.println(number_1 + number_2);
        System.out.println(--number_1 + number_2++);
        System.out.println(number_1 + number_2);
    }
}